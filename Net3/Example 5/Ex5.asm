	AREA PROGRAM,CODE,READONLY
ENTRY
MAIN
	LDRB R1,x
	LDRH R2,y
	LDR R4,=RESULT
	SUB R3,R1,R2
	STRH R3,[R4]
	ADD R4,R4,#4
	RSB R3,R1,R2
	STRH R3,[R4]
	ADD R4,R4,#4
	SBC R3,R1,R2
	STRH R3,[R4]
	ADD R4,R4,#4
stop b stop
x	DCW	-0X0001
y	DCW -0x0001

	AREA EXAMPLE,DATA,READWRITE
ALIGN
RESULT	DCW 0
	END
